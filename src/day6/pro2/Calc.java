package day6.pro2;

public class Calc {
//  加算
    double add(double a,double b){
        return a + b;
    }
    //  加算
    double sub(double a,double b){
        return a - b;
    }
    double mull(double a,double b) {
		return a * b;
    }
    double div(double a, double b) {
		return a / b;
    }
}
